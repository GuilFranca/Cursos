package udemy;

public class Programa06 {

	public static void main(String[] args) {
		int num = 9;
		
		switch (num) {
			case 1: 
				System.out.println("O número é 1");
				break;
			case 3:
				System.out.println("O número é 2");
				break;
			case 5:
				System.out.println("O número é 5");
				break;
			default:
				System.out.println("O númeor é " + num);
				break;
		}
	}
	
}